package two_operatorsUse;

public class Assingment_operator {

	public static void main(String[] args) {
		// these are those operator required in assingment activity.
		int a = 10;
		a+= 2; // ther we mean a = a+2. similarly.......
		System.out.println(a);
		a-=2;
		System.out.println(a);
		a*=2;
		System.out.println(a);
		a/=2; // and so on .......
		System.out.println(a);
		a>>=2;
		System.out.println(a);
		a&=2;		
		System.out.println(a);
	}

}
